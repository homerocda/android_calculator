package msd.prog8170.assignment3.newcalculator

/**
 * The calculator class.
 */
class Calculator {

    /**
     * Performs addition.
     */
    fun addition(lhs: Double, rhs: Double) = lhs + rhs

    /**
     * Performs subtraction.
     */
    fun subtraction(lhs: Double, rhs: Double) = lhs - rhs

    /**
     * Performs multiplication.
     */
    fun multiplication(lhs: Double, rhs: Double) = lhs * rhs

    /**
     * Performs division.
     */
    fun division(lhs: Double, rhs: Double) = lhs / rhs
}